uint16_t post_address = 0x0000;
uint16_t pf1_post_address = 0xA030;
uint16_t hex_post_address = 0x8000;

void parse_hex_line(const char* line) {
    if (line[0] != ':') return;
    const char* p = line + 1;

    uint8_t byte_count = hex_to_byte(p);       p += 2;
    uint16_t address = (hex_to_byte(p) << 8) | hex_to_byte(p + 2); p += 4;
    uint8_t record_type = hex_to_byte(p);      p += 2;


    //uint32_t data1, data2;
    if (record_type == 0x00) {
        uint8_t data_bytes[256] = {0};  // 최대 256바이트
        //uint64_t to_write_data = 0x0;
        for (int i = 0; i < byte_count; i++) {
            data_bytes[i] = hex_to_byte(p); p+=2;
//            my_printf("%02X ", data_byte);
        }
        uint32 target_address = pf1_post_address << 16 | address;
        my_printf("data address : %04X", target_address);
        g_commandFromPSPR.writeFlash(byte_count, data_bytes, target_address);
    }
    else if (record_type == 0x04) {
        for (int i = 0; i < byte_count; i++) {
            post_address = (post_address << 8) | hex_to_byte(p);
            p += 2;
        }
        uint16_t gap = (post_address-hex_post_address);
        pf1_post_address +=  gap;
        my_printf("post address : %04X", pf1_post_address);
    }
    else {
        my_printf("  (Non-data record %u, skipped)", record_type);
    }
    my_printf("\n");
}

