#define UCB_SWAP_ORIG_MARKERL0 0xAF402E00
#define UCB_SWAP_ORIG_MARKERH0 0xAF402E04

#define UCB_SWAP_COPY_MARKERL0 0xAF403E00
#define UCB_SWAP_COPY_MARKERH0 0xAF403E04

#define UCB_SWAP_ORIG_CONFIRMATIONL0 0xAF402E08
#define UCB_SWAP_ORIG_CONFIRMATIONH0 0xAF402E0C

#define UCB_SWAP_COPY_CONFIRMATIONL0 0xAF403E08
#define UCB_SWAP_COPY_CONFIRMATIONH0 0xAF40230C

#define UCB_OTP0_ORIG_SWAPEN 0xAF4041E8
#define UCB_OTP0_COPY_SWAPEN 0xAF4051E8
void writeUCB(uint32 pageAddr, uint32 value1, uint32 value2){
    uint16 endInitSafetyPassword = IfxScuWdt_getSafetyWatchdogPassword();
    /* Enter in page mode */
    IfxFlash_enterPageMode(pageAddr);

    /* Wait until page mode is entered */
    IfxFlash_waitUnbusy(FLASH_MODULE, DATA_FLASH_0);

    /* Load data to be written in the page */
    IfxFlash_loadPage2X32(pageAddr, value1, value2); /* Load two words of 32 bits each            */

    /* Write the loaded page */
    IfxScuWdt_clearSafetyEndinit(endInitSafetyPassword);    /* Disable EndInit protection                       */
    IfxFlash_writePage(pageAddr);                           /* Write the page                                   */
    IfxScuWdt_setSafetyEndinit(endInitSafetyPassword);      /* Enable EndInit protection                        */

    /* Wait until the data is written in the Data Flash memory */
    IfxFlash_waitUnbusy(FLASH_MODULE, DATA_FLASH_0);
}

void swap_pf0_to_pf1(){
    //    swap pf0 to pf1
    uint8 ucb_swap_x_idx = 0;
    uint32 UCB_SWAP_ORIG_MARKERL = UCB_SWAP_ORIG_MARKERL0;
    uint32 UCB_SWAP_ORIG_CONFIRMATIONL = UCB_SWAP_ORIG_CONFIRMATIONL0;

    uint32 UCB_SWAP_COPY_MARKERL = UCB_SWAP_COPY_MARKERL0;
    uint32 UCB_SWAP_COPY_CONFIRMATIONL = UCB_SWAP_COPY_CONFIRMATIONL0;
    while (MEM(UCB_SWAP_ORIG_CONFIRMATIONL) != 0x00){
        UCB_SWAP_ORIG_MARKERL += 0x10;
        UCB_SWAP_COPY_MARKERL += 0x10;

        UCB_SWAP_ORIG_CONFIRMATIONL += 0x10;
        UCB_SWAP_COPY_CONFIRMATIONL +=0x10;
        ucb_swap_x_idx+=1;
    }
    uint32 UCB_SWAP_ORIG_CONFIRMATION_past = UCB_SWAP_ORIG_CONFIRMATIONL0;
    uint32 UCB_SWAP_COPY_CONFIRMATION_past = UCB_SWAP_COPY_CONFIRMATIONL0;
    for (int i=0; i < ucb_swap_x_idx; i++){
        writeUCB(UCB_SWAP_ORIG_CONFIRMATION_past, 0xFFFFFFFF, 0xFFFFFFFF); //UCB_SWAP_ORIG_CONFIRMATIONL/H overwritten by All-1
        writeUCB(UCB_SWAP_COPY_CONFIRMATION_past, 0xFFFFFFFF, 0xFFFFFFFF); //UCB_SWAP_ORIG_CONFIRMATIONL/H overwritten by All-1
        UCB_SWAP_ORIG_CONFIRMATION_past += 0x10;
        UCB_SWAP_COPY_CONFIRMATION_past += 0x10;
    }
    writeUCB(UCB_SWAP_ORIG_MARKERL, 0x000000AA, UCB_SWAP_ORIG_MARKERL); //MARKERL & MARKERH
    writeUCB(UCB_SWAP_COPY_MARKERL, 0x000000AA, UCB_SWAP_COPY_MARKERL); //COPY_MARKERL & COPY_MARKERH
    writeUCB(UCB_SWAP_ORIG_CONFIRMATIONL, 0x57B5327F, UCB_SWAP_ORIG_CONFIRMATIONL); //UCB_SWAP_ORIG_CONFIRMATIONL & UCB_SWAP_ORIG_CONFIRMATIONH
    writeUCB(UCB_SWAP_COPY_CONFIRMATIONL, 0x57B5327F, UCB_SWAP_COPY_CONFIRMATIONL); //UCB_SWAP_COPY_CONFIRMATIONL & UCB_SWAP_COPY_CONFIRMATIONH


    writeUCB(UCB_OTP0_ORIG_SWAPEN, 0x00030000, 0x57B5327F);
    writeUCB(UCB_OTP0_COPY_SWAPEN, 0x00030000, 0x57B5327F);
}
void swap_pf1_to_pf0(){
    //    swap pf1 to pf0
    uint8 ucb_swap_x_idx = 0;
    uint32 UCB_SWAP_ORIG_MARKERL = UCB_SWAP_ORIG_MARKERL0;
    uint32 UCB_SWAP_ORIG_CONFIRMATIONL = UCB_SWAP_ORIG_CONFIRMATIONL0;

    uint32 UCB_SWAP_COPY_MARKERL = UCB_SWAP_COPY_MARKERL0;
    uint32 UCB_SWAP_COPY_CONFIRMATIONL = UCB_SWAP_COPY_CONFIRMATIONL0;

    for (int i=0 ; i < 16 ; i++){
        if (MEM(UCB_SWAP_ORIG_CONFIRMATIONL) != 0x00){
            UCB_SWAP_ORIG_MARKERL += 0x10;
            UCB_SWAP_COPY_MARKERL += 0x10;

            UCB_SWAP_ORIG_CONFIRMATIONL += 0x10;
            UCB_SWAP_COPY_CONFIRMATIONL +=0x10;
            ucb_swap_x_idx+=1;
        }
        else{
            break;
        }
        if (i==15 && MEM(UCB_SWAP_ORIG_CONFIRMATIONL) != 0x00){
            UCB_SWAP_ORIG_MARKERL = UCB_SWAP_ORIG_MARKERL0;
            UCB_SWAP_ORIG_CONFIRMATIONL = UCB_SWAP_ORIG_CONFIRMATIONL0;

            UCB_SWAP_COPY_MARKERL = UCB_SWAP_COPY_MARKERL0;
            UCB_SWAP_COPY_CONFIRMATIONL = UCB_SWAP_COPY_CONFIRMATIONL0;

            uint16 endInitSafetyPassword = IfxScuWdt_getSafetyWatchdogPassword();
            IfxScuWdt_clearSafetyEndinit(endInitSafetyPassword);        /* Disable EndInit protection                       */
            IfxFlash_eraseMultipleSectors(DFLASH_STARTING_ADDRESS, DFLASH_NUM_SECTORS); /* Erase the given sector           */
            IfxScuWdt_setSafetyEndinit(endInitSafetyPassword);          /* Enable EndInit protection                        */
            ucb_swap_x_idx =0;
        }
    }
    uint32 UCB_SWAP_ORIG_CONFIRMATION_past = UCB_SWAP_ORIG_CONFIRMATIONL0;
    uint32 UCB_SWAP_COPY_CONFIRMATION_past = UCB_SWAP_COPY_CONFIRMATIONL0;
    for (int i=0; i < ucb_swap_x_idx; i++){
        writeUCB(UCB_SWAP_ORIG_CONFIRMATION_past, 0xFFFFFFFF, 0xFFFFFFFF); //UCB_SWAP_ORIG_CONFIRMATIONL/H overwritten by All-1
        writeUCB(UCB_SWAP_COPY_CONFIRMATION_past, 0xFFFFFFFF, 0xFFFFFFFF); //UCB_SWAP_ORIG_CONFIRMATIONL/H overwritten by All-1
        UCB_SWAP_ORIG_CONFIRMATION_past += 0x10;
        UCB_SWAP_COPY_CONFIRMATION_past += 0x10;
    }
    writeUCB(UCB_SWAP_ORIG_MARKERL, 0x00000055, UCB_SWAP_ORIG_MARKERL); //MARKERL & MARKERH
    writeUCB(UCB_SWAP_COPY_MARKERL, 0x00000055, UCB_SWAP_COPY_MARKERL); //COPY_MARKERL & COPY_MARKERH
    writeUCB(UCB_SWAP_ORIG_CONFIRMATIONL, 0x57B5327F, UCB_SWAP_ORIG_CONFIRMATIONL); //UCB_SWAP_ORIG_CONFIRMATIONL & UCB_SWAP_ORIG_CONFIRMATIONH
    writeUCB(UCB_SWAP_COPY_CONFIRMATIONL, 0x57B5327F, UCB_SWAP_COPY_CONFIRMATIONL); //UCB_SWAP_COPY_CONFIRMATIONL & UCB_SWAP_COPY_CONFIRMATIONH

    writeUCB(UCB_OTP0_ORIG_SWAPEN, 0x00030000, 0x57B5327F);
    writeUCB(UCB_OTP0_COPY_SWAPEN, 0x00030000, 0x57B5327F);
}

